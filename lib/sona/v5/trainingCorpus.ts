// دالة للحصول على جميع النصوص
// ============================================

export function getAllCorpus(): string {
  return [
    BIRTHDAY_CORPUS,
    ZODIAC_CORPUS,
    HEALTH_CORPUS,
    GENERAL_CORPUS,
    DATES_CORPUS,
  ].join('\n\n');
}

export function getCorpusByCategory(category: string): string {
  switch (category) {
    case 'birthday':
      return BIRTHDAY_CORPUS;
    case 'zodiac':
      return ZODIAC_CORPUS;
    case 'health':
      return HEALTH_CORPUS;
    case 'dates':
      return DATES_CORPUS;
    default:
      return GENERAL_CORPUS;
  }
}
