export function getRandomPhrase(
  category: keyof typeof MASSIVE_INTROS,
  topic?: string
): string {
  const phrases = MASSIVE_INTROS[category];
  if (!phrases || phrases.length === 0) return '';

  const phrase = phrases[Math.floor(Math.random() * phrases.length)];
  return topic ? phrase.replace(/{topic}/g, topic) : phrase;
}

export function getRandomTransition(): string {
  return MASSIVE_TRANSITIONS[
    Math.floor(Math.random() * MASSIVE_TRANSITIONS.length)
  ];
}

export function getRandomConclusion(
  type: keyof typeof MASSIVE_CONCLUSIONS
): string {
  const conclusions = MASSIVE_CONCLUSIONS[type];
  if (!conclusions || conclusions.length === 0) return '';
  return conclusions[Math.floor(Math.random() * conclusions.length)];
}

