export function generateTableOfContents(content: string): string[] {
  const headings: string[] = [];
  const h2Regex = /<h2[^>]*>([^<]+)<\/h2>/gi;
  let match;

  while ((match = h2Regex.exec(content)) !== null) {
    headings.push(match[1].trim());
  }

  return headings;
}

