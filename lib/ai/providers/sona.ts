// تحديد نوع المحتوى من الموضوع
function detectContentType(topic: string): 'birthday' | 'zodiac' | 'general' {
  if (
    topic.includes('عيد ميلاد') ||
    topic.includes('ميلاد') ||
    topic.includes('birthday')
  ) {
    return 'birthday';
  }

  for (const sign of Object.keys(ZODIAC_DATA)) {
    if (topic.includes(sign) || topic.includes('برج')) {
      return 'zodiac';
    }
  }

  return 'general';
}

// استخراج البرج من الموضوع
function extractZodiacSign(topic: string): string | null {
  for (const sign of Object.keys(ZODIAC_DATA)) {
    if (topic.includes(sign)) {
      return sign;
    }
  }
  return null;
}

// توليد العنوان
function generateTitle(topic: string, contentType: string): string {
  switch (contentType) {
    case 'birthday':
      return `${topic} - دليلك الشامل للاحتفال`;
    case 'zodiac':
      const sign = extractZodiacSign(topic);
      return sign ? `برج ${sign} - صفاته وتوافقه وحظه الكامل` : topic;
    default:
      return `${topic} - دليلك الشامل`;
  }
}

// استخراج الكلمات المفتاحية
function extractKeywords(content: string, topic: string): string[] {
  const keywords = new Set<string>();
  keywords.add(topic);

  // كلمات شائعة
  const commonKeywords = [
    'عيد ميلاد',
    'برج',
    'حظ',
    'توافق',
    'صفات',
    'شخصية',
    'حجر الميلاد',
    'زهرة الميلاد',
  ];
  commonKeywords.forEach((k) => {
    if (content.includes(k)) keywords.add(k);
  });

  // الأبراج
  Object.keys(ZODIAC_DATA).forEach((z) => {
    if (content.includes(z)) keywords.add(z);
  });

  return Array.from(keywords).slice(0, 15);
}

