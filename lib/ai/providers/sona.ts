// استخراج كلمات مفتاحية ذكية من قاعدة بيانات ميلادك
function extractSmartKeywords(
  topic: string,
  analysis: TopicAnalysis
): string[] {
  const keywords = new Set<string>();

  // إضافة الموضوع الأساسي
  keywords.add(topic);

  // إضافة كلمات ميلادك الأساسية
  MILADAK_KEYWORDS.miladak.slice(0, 3).forEach((k) => keywords.add(k));

  // إضافة الكلمات المفتاحية من التحليل
  analysis.keywords.forEach((k) => keywords.add(k));

  // ربط التصنيفات بقاعدة الكلمات المفتاحية
  const categoryToKeywords: Record<
    TopicCategory,
    keyof typeof MILADAK_KEYWORDS
  > = {
    birthday: 'birthday',
    zodiac: 'zodiac',
    age_calculator: 'age',
    date_converter: 'dates',
    health: 'health',
    pregnancy: 'pregnancy',
    names: 'names',
    celebrations: 'events',
    colors_numbers: 'zodiac',
    historical: 'events',
    celebrities: 'events',
    general: 'miladak',
  };

  // إضافة كلمات من قاعدة البيانات
  const keywordCategory = categoryToKeywords[analysis.category];
  if (keywordCategory && MILADAK_KEYWORDS[keywordCategory]) {
    const categoryKws = MILADAK_KEYWORDS[keywordCategory];
    // اختيار كلمات عشوائية من القاعدة
    const shuffled = [...categoryKws].sort(() => Math.random() - 0.5);
    shuffled.slice(0, 10).forEach((k) => keywords.add(k));
  }

  // إضافة كلمات ذات صلة بالموضوع
  const topicWords = topic.split(' ');
  Object.values(MILADAK_KEYWORDS)
    .flat()
    .forEach((kw) => {
      if (topicWords.some((word) => kw.includes(word) && word.length > 2)) {
        keywords.add(kw);
      }
    });

  return Array.from(keywords).slice(0, 20);
}

// إعادة صياغة المحتوى
export async function rewriteContent(
  content: string,
  style: string
): Promise<{
  original: string;
  rewritten: string;
  wordCount: number;
  provider: 'sona';
}> {
  // قاموس المرادفات المحسّن
  const synonyms: Record<string, string[]> = {
    جميل: ['رائع', 'بديع', 'ساحر', 'فاتن', 'أخاذ'],
    كبير: ['ضخم', 'هائل', 'عظيم', 'واسع', 'كثير'],
    صغير: ['ضئيل', 'قليل', 'محدود', 'بسيط', 'يسير'],
    سعيد: ['مبتهج', 'فرح', 'مسرور', 'منشرح', 'سعيد'],
    مهم: ['ضروري', 'أساسي', 'جوهري', 'حيوي', 'هام'],
    يقول: ['يذكر', 'يصرح', 'يوضح', 'يبين', 'يشير'],
    يعمل: ['يشتغل', 'يمارس', 'ينجز', 'يؤدي', 'يقوم'],
    يريد: ['يرغب', 'يتمنى', 'يود', 'يطمح', 'يسعى'],
    جيد: ['ممتاز', 'حسن', 'طيب', 'مناسب', 'رائع'],
    سيء: ['رديء', 'ضعيف', 'متدني', 'غير مناسب', 'سلبي'],
    يساعد: ['يعين', 'يدعم', 'يسهم', 'يشارك', 'يعاون'],
    يفهم: ['يدرك', 'يستوعب', 'يعي', 'يتفهم', 'يستنتج'],
  };

  let rewritten = content;

  // استبدال المرادفات
  for (const [word, alternatives] of Object.entries(synonyms)) {
    const regex = new RegExp(`\\b${word}\\b`, 'g');
    const replacement =
      alternatives[Math.floor(Math.random() * alternatives.length)];
    rewritten = rewritten.replace(regex, replacement);
  }

  // تعديل حسب النمط
  if (style === 'formal') {
    rewritten = rewritten.replace(/أنت/g, 'حضرتك');
    rewritten = rewritten.replace(/أنتم/g, 'حضراتكم');
  } else if (style === 'simplified') {
    rewritten = rewritten.replace(/بالإضافة إلى ذلك/g, 'وأيضاً');
    rewritten = rewritten.replace(/علاوة على ذلك/g, 'وكذلك');
  }

  return {
    original: content,
    rewritten,
    wordCount: rewritten.split(/\s+/).length,
    provider: 'sona',
  };
}

