// دالة للحصول على موضوع عشوائي للتوليد التلقائي
export function getRandomArticleTopic(category?: string): string {
  const categories = Object.keys(SUGGESTED_ARTICLE_TOPICS);
  const selectedCategory =
    category || categories[Math.floor(Math.random() * categories.length)];
  const topics =
    SUGGESTED_ARTICLE_TOPICS[
      selectedCategory as keyof typeof SUGGESTED_ARTICLE_TOPICS
    ] || SUGGESTED_ARTICLE_TOPICS.general;
  return topics[Math.floor(Math.random() * topics.length)];
}

// دالة للحصول على قائمة مواضيع حسب الفئة
export function getTopicsByCategory(category: string): string[] {
  return (
    SUGGESTED_ARTICLE_TOPICS[
      category as keyof typeof SUGGESTED_ARTICLE_TOPICS
    ] || []
  );
}

// دالة للحصول على جميع المواضيع المقترحة
export function getAllSuggestedTopics(): string[] {
  return Object.values(SUGGESTED_ARTICLE_TOPICS).flat();
}

