// دالة مساعدة لتحليل جودة المحتوى
export function analyzeContentQuality(content: string): {
  wordCount: number;
  headingsCount: number;
  paragraphsCount: number;
  listsCount: number;
  readabilityScore: number;
  seoScore: number;
} {
  const textContent = content.replace(/<[^>]*>/g, ' ').trim();
  const wordCount = textContent.split(/\s+/).filter(Boolean).length;
  const headingsCount = (content.match(/<h[1-6][^>]*>/gi) || []).length;
  const paragraphsCount = (content.match(/<p[^>]*>/gi) || []).length;
  const listsCount = (content.match(/<[uo]l[^>]*>/gi) || []).length;

  // حساب درجة القراءة (مبسط)
  const avgSentenceLength =
    textContent.split(/[.،!؟]/).length > 0
      ? wordCount / textContent.split(/[.،!؟]/).length
      : 0;
  const readabilityScore = Math.min(
    100,
    Math.max(0, 100 - (avgSentenceLength - 15) * 2)
  );

  // حساب درجة SEO (مبسط)
  let seoScore = 0;
  if (wordCount >= 500) seoScore += 20;
  if (wordCount >= 1000) seoScore += 20;
  if (headingsCount >= 3) seoScore += 20;
  if (paragraphsCount >= 5) seoScore += 20;
  if (listsCount >= 1) seoScore += 20;

  return {
    wordCount,
    headingsCount,
    paragraphsCount,
    listsCount,
    readabilityScore: Math.round(readabilityScore),
    seoScore,
  };
}

