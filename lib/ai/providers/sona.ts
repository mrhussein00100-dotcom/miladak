// استخراج المتغيرات من الموضوع
function extractVariables(
  topic: string,
  category: TopicCategory
): Record<string, any> {
  const variables: Record<string, any> = {};

  // استخراج الأرقام (العمر، السنة، إلخ)
  const numbers = topic.match(/\d+/g);
  if (numbers) {
    variables.numbers = numbers.map((n) => parseInt(n));
    if (category === 'birthday' || category === 'age_calculator') {
      variables.age = parseInt(numbers[0]);
    }
  }

  // استخراج الأسماء (بعد كلمات معينة)
  const namePatterns = [/عيد ميلاد\s+(\S+)/, /تهنئة\s+(\S+)/, /اسم\s+(\S+)/];
  for (const pattern of namePatterns) {
    const match = topic.match(pattern);
    if (match) {
      variables.name = match[1];
      break;
    }
  }

  // استخراج البرج
  const zodiacSigns = [
    'الحمل',
    'الثور',
    'الجوزاء',
    'السرطان',
    'الأسد',
    'العذراء',
    'الميزان',
    'العقرب',
    'القوس',
    'الجدي',
    'الدلو',
    'الحوت',
  ];
  for (const sign of zodiacSigns) {
    if (topic.includes(sign)) {
      variables.zodiacSign = sign;
      break;
    }
  }

  return variables;
}

